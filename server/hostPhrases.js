export const PHRASES = {
  lobby: {
    waiting: (count, need) =>
      `В комнате ${count} из ${need} игроков. Ждём остальных.`,
    ready: (need) =>
      `Все на месте. Для начала игры нужно минимум ${need} человек. Нажмите «Начать игру», когда будете готовы.`,
    started: 'Игра начинается. Сейчас каждому будет назначена роль. Смотрите на свой экран — никому не показывайте роль.',
  },

  roles: {
    youAre: (role) => {
      const names = {
        mafia: 'Мафия',
        doctor: 'Доктор',
        detective: 'Детектив',
        civilian: 'Мирный житель',
      };
      return `Ваша роль: ${names[role] || role}. Запомните и никому не говорите.`;
    },
    rolesAssigned: 'Роли распределены. Передайте телефон ведущему или подключите к колонке. Ночь через три секунды.',
  },

  night: {
    closeEyes: 'Город засыпает. Все закрывают глаза.',
    mafiaWake: 'Мафия, проснитесь. Узнайте друг друга. Выберите одну жертву на эту ночь.',
    mafiaSleep: 'Мафия, засыпайте.',
    doctorWake: 'Доктор, проснись. Выбери, кого хочешь спасти этой ночью.',
    doctorSleep: 'Доктор, засыпай.',
    detectiveWake: 'Детектив, проснись. Укажи игрока — я скажу, мафия он или нет.',
    detectiveSleep: 'Детектив, засыпай.',
    morning: 'Рассвет. Все открывают глаза.',
  },

  day: {
    nobodyKilled: 'Этой ночью никто не погиб. Обсуждение — одна минута.',
    killed: (name) => `Этой ночью погиб: ${name}. Он выбывает из игры. Обсуждение — одна минута.`,
    discussionEnd: 'Обсуждение закончено. Голосование: кого исключаем? У каждого один голос.',
    voteResult: (name, roleKey) => {
      const roleNames = { mafia: 'мафией', doctor: 'доктором', detective: 'детективом', civilian: 'мирным жителем' };
      return `Исключён по результатам голосования: ${name}. Его роль — ${roleNames[roleKey] || 'мирный житель'}.`;
    },
    tie: 'Равное количество голосов. Никто не исключён. Переходим к ночи.',
  },

  end: {
    mafiaWins: 'Мафия победила. Поздравляю мафию!',
    civiliansWin: 'Мирные жители победили. Город спасён!',
  },

  host: {
    nextPhase: (phase) => `Переходим к фазе: ${phase}.`,
    listenToHost: 'Внимание на ведущего. Слушайте объявления и действуйте по инструкциям на экране.',
  },
};

export function getPhrase(key, ...args) {
  const path = key.split('.');
  let v = PHRASES;
  for (const p of path) v = v?.[p];
  return typeof v === 'function' ? v(...args) : v;
}
